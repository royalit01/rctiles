<?php
// Initialize a variable to check if the category was successfully added
$categoryAdded = false;

// Check for form submission
if ($_SERVER["REQUEST_METHOD"] == "POST" && isset($_POST['add_category'])) {
    $category_id = $_POST['category_id'];
    $category_name = $_POST['category_name'];

    // Database connection
    $servername = "localhost";
    $username = "root";
    $password = "";
    $dbname = "rc_ceramic_mall_db";
    $mysqli = new mysqli($servername, $username, $password, $dbname);

    // Check connection
    if ($mysqli->connect_error) {
        die("Connection failed: " . $mysqli->connect_error);
    }

    // Prepare and bind
    $stmt = $mysqli->prepare("INSERT INTO category (category_id, category_name) VALUES (?, ?)");
    $stmt->bind_param("is", $category_id, $category_name);

    // Execute and feedback
    if ($stmt->execute()) {
        $categoryAdded = true;
    } else {
        echo "<div class='alert alert-danger'>Error: " . $stmt->error . "</div>";
    }

    $stmt->close();
    $mysqli->close();
}
